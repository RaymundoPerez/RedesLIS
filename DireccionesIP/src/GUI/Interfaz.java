    private void buttonGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonGenerarActionPerformed
        if(ip1.getText().isEmpty() || ip2.getText().isEmpty() || ip3.getText().isEmpty() || ip4.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Introduce un direccion IPv4 valida");
        }else{
            int ipSeccion1 = Integer.parseInt(ip1.getText());
            int ipSeccion2 = Integer.parseInt(ip2.getText());
            int ipSeccion3 = Integer.parseInt(ip3.getText());
            int ipSeccion4 = Integer.parseInt(ip4.getText());
            if(ipSeccion2 < 256 && ipSeccion3 <256 && ipSeccion4 < 256){
                if(ipSeccion1 < 128){
                    labelClase.setText("A");
                    labelMascara.setText("255.0.0.0");
                    labelMascara2.setText("/8");
                    labelDireccionRed.setText(ipSeccion1+"."+"0.0.0");
                    labelDireccionBroadcast.setText(ipSeccion1+"."+"255.255.255");
                }else if(ipSeccion1 < 192){
                    labelClase.setText("B");
                    labelMascara.setText("255.255.0.0");
                    labelMascara2.setText("/16");
                    labelDireccionRed.setText(ipSeccion1+"."+ipSeccion2+"."+"0.0");
                    labelDireccionBroadcast.setText(ipSeccion1+"."+ipSeccion2+"."+"255.255");
                }else if(ipSeccion1 < 224){
                    labelClase.setText("C");
                    labelMascara.setText("255.255.255.0");
                    labelMascara2.setText("/24");
                    labelDireccionRed.setText(ipSeccion1+"."+ipSeccion2+"."+ipSeccion3+"."+"0");
                    labelDireccionBroadcast.setText(ipSeccion1+"."+ipSeccion2+"."+ipSeccion3+"."+"255");
                }else{
                    JOptionPane.showMessageDialog(null, "Introduce un direccion IPv4 valida (Clase A,B o C)");
                }// TODO add your handling code here:
            }else{
                JOptionPane.showMessageDialog(null, "Introduce un direccion IPv4 valida");
            }
        }
    }//GEN-LAST:event_buttonGenerarActionPerformed

